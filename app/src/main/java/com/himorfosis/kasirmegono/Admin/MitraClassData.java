package com.himorfosis.kasirmegono.Admin;

public class MitraClassData {

    private Integer id_mitra;
    private String nama_mitra;
    private String email;
    private String password;

    public Integer getId_mitra() {
        return id_mitra;
    }

    public void setId_mitra(Integer id_mitra) {
        this.id_mitra = id_mitra;
    }

    public String getNama_mitra() {
        return nama_mitra;
    }

    public void setNama_mitra(String nama_mitra) {
        this.nama_mitra = nama_mitra;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
